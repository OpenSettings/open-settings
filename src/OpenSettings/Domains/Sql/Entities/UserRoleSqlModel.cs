using System.Collections.Generic;
using System.ComponentModel.DataAnnotations.Schema;

namespace OpenSettings.Domains.Sql.Entities
{
    /// <summary>
    /// Represents a user role entity.
    /// </summary>
    [Table("UserRoles")]
    public class UserRoleSqlModel : EntityBase<int>
    {
        /// <summary>
        /// The name of the user role.
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// The lowercase version of the <see cref="Name"/>, typically used for case-insensitive comparisons.
        /// </summary>
        public string NameLowercase { get; set; }

        /// <summary>
        /// An url friendly version of the <see cref="Name"/>, generated by trimming, converting to lowercase,  
        /// and replacing spaces or special characters with hyphens (using <see cref="Extensions.InternalExtensions.ToSlug"/> extension method).
        /// </summary>
        public string Slug { get; set; }

        /// <summary>
        /// The collection of user role-claim mappings associated with this user role.
        /// </summary>
        public virtual ICollection<UserRoleClaimMappingModel> RoleClaimMappings { get; set; } = new List<UserRoleClaimMappingModel>();

        /// <summary>
        /// The collection of user-role mappings associated with this user role.
        /// </summary>
        public virtual ICollection<UserRoleMappingSqlModel> UserRoleMappings { get; set; } = new List<UserRoleMappingSqlModel>();

        /// <summary>
        /// The collection of role-group mappings associated with this user role.
        /// </summary>
        public virtual ICollection<UserRoleGroupMappingModel> RoleGroupMappings { get; set; } = new List<UserRoleGroupMappingModel>();
    }
}